function parseStringToNumberArray(str: string) {
  const array = str.split("\n");
  const numbers: number[] = [];

  for (const item of array) {
    if (!item.includes("decoration")) continue;
    const string = item
      .replace(/[0-9]?markdown-decoration.ts:128 /, "")
      .replace("decoration: ", "")
      .replace(" ms", "");

    if (!Number.isNaN(+string)) numbers.push(+string);
  }

  const max = Math.max.apply(null, numbers);
  const min = Math.min.apply(null, numbers);

  const averageF = (arr: number[]) => arr.reduce((p, c) => p + c, 0) / arr.length;
  const average = averageF(numbers);

  return { max, min, average };
}

